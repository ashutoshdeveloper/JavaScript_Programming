// DEmonstrate the use of switch case in JS
let student = "Manish";
switch(student){
    case "Ram":
        console.log("The student name is Ram")
        break;
    case "Rahul":
        console.log("The student name is Rahul")    
        break;
    case "Ajay":
        console.log("The student name is Ajay")  
        break; 
    case "Manish":
        console.log("The student name is Manish")  
        break; 
    case "Apurv":
        console.log("The student name is Apurv")  
        break; 

    default:
        console.log("The student name is not found")   
        break; 
    
}